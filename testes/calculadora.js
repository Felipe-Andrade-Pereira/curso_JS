let numero1 = 1003;
let numero2 = 9;
let operador = '-';

if (operador === '+'){
    console.log('O resultado de '+numero1+' ' + operador+' ' +numero2+' é: '+numero1 + numero2);
}else if(operador === '-'){
    console.log('O resultado de '+numero1+' ' + operador+' ' +numero2+' é: '+numero1 * numero2);
}else if(operador === '*'){
    console.log('O resultado de '+numero1+' ' + operador+' ' +numero2+' é: '+numero1 * numero2);
}else if(operador === '/'){
    console.log('O resultado de '+numero1+' ' + operador+' ' +numero2+' é: '+numero1 / numero2);
}